public class Client
{
    public String nume;
    public String gen;
    public int varsta;
    public float cnp;


    public String getNume()
    {
        return nume;
    }
    public void setNume(String nume)
    {
        this.nume=nume;
    }
    public String getGen()
    {
        return gen;
    }
    public void setGen(String varsta)
    {
        this.gen=varsta;
    }
    public int getVarsta()
    {
        return varsta;
    }
    public void setVarsta(int varsta)
    {
        this.varsta=varsta;
    }
    public float getCnp()
    {
        return cnp;
    }
    public void setCnp(float cnp)
    {
        this.cnp=cnp;
    }
}
